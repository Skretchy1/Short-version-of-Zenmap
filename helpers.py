def ip_checkv4(ip):
	parts = ip.split(".")
	if len(parts) < 4 or len(parts) > 4:
		return False
	else:
		while len(parts) == 4:
			a = int(parts[0])
			b = int(parts[1])
			c = int(parts[2])
			d = int(parts[3])
			if a <= 0 or a == 127:
				return False
			elif d == 0:
				return False
			elif a >= 255:
				return False
			elif b >= 255 or b < 0:
				return False
			elif c >= 255 or c < 0:
				return False
			elif d >= 255 or c < 0:
				return False
			else:
				return True
